#!/bin/bash



# 1. Запит назви існуючого файлу

echo -n "Введіть назву існуючого файлу: "

read old_name



# 2. Перевірка наявності об'єкта

if [[ ! -f "$old_name" ]]; then

    echo "Помилка: файл '$old_name' не знайдено."

    exit 1

fi



# 3. Запит нової назви

echo -n "Введіть нову назву для файлу: "

read new_name



# 4. Перевірка обмеження на довжину

if [[ ${#new_name} -gt 11 ]]; then

    echo "Помилка: назва занадто довга (максимум 11 символів)."

    exit 1

fi



# 5. Перевірка на зміст назви

if [[ "$new_name" =~ [0-9]{9,} ]]; then

    echo "Помилка: назва містить більше 8 цифр підряд."

    exit 1

fi



# 6. Перевірка, чи не зайнята нова назва

if [[ -e "$new_name" ]]; then

    echo "Помилка: об'єкт з назвою '$new_name' вже існує."

    exit 1

fi



# 7. Виконання перейменування

mv "$old_name" "$new_name"

echo "Файл успішно перейменовано на '$new_name'."
